# calc_one.py

def calc_sum(num1, num2):
    calcSum = num1 + num2
    return calcSum

def calc_sub(num1, num2):
    calcSub = num1 - num2
    return calcSub

def calc_mult(num1, num2):
    calcMult = num1 * num2
    return calcMult

def calc_div(num1, num2):
    calcDiv = num1 / num2
    return calcDiv

def calc_mod(num1, num2):
    calcMod = num1 % num2
    return calcMod
